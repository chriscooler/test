#include <unistd.h>

char	*ft_strcpy(char *dest, char *src)
{
	int i;

	i = 0;
	while(src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

int main(void) 
{
   	char str[] = "asdfasdfasdf";
	char dest[] = "there";
	
	printf("%s", ft_strcpy(dest, str));
	return 0;
}
